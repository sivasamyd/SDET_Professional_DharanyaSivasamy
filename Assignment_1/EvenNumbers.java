package program1;

public class EvenNumbers {
		public static void main(String args[])   
	{  
	int number=50;  
	System.out.print("List of even numbers from 1 to "+number+": ");  
	for (int i=1; i<=number; i++)   
	{  
	//logic to check if the number is even or not  
	if (i%2==0)   
	{  
	System.out.print(i + " ");  
	}  
	}  
	}  
	
}
